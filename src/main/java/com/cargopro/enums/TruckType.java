package com.cargopro.enums;

/**
 * Enum representing different types of trucks
 * Used to match transporter's truck type with load requirements
 */
public enum TruckType {
    SMALL,      // Small trucks (e.g., pickup trucks)
    MEDIUM,     // Medium trucks (e.g., box trucks)
    LARGE,      // Large trucks (e.g., semi-trailers)
    HEAVY       // Heavy-duty trucks (e.g., flatbeds)
}

